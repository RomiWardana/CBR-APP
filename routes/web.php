<?php

use Illuminate\Support\Facades\Route;
use App\Http\Controllers\CBRController;

/*
|--------------------------------------------------------------------------
| HOME (ENTRY UTAMA)
|--------------------------------------------------------------------------
*/
Route::get('/', function () {
    return view('home');
});

/*
|--------------------------------------------------------------------------
| LOGIN
|--------------------------------------------------------------------------
*/
Route::get('/login', function () {
    return view('login');
});

Route::post('/login', [CBRController::class, 'login']);

/*
|--------------------------------------------------------------------------
| REGISTER
|--------------------------------------------------------------------------
*/
Route::get('/register', function () {
    return view('register');
});

Route::post('/register', [CBRController::class, 'register']);

/*
|--------------------------------------------------------------------------
| INDEX (MENU SETELAH LOGIN)
|--------------------------------------------------------------------------
*/
Route::get('/index', function () {

    if (!session()->has('login')) {
        return redirect('/login');
    }

    return view('index');
});

/*
|--------------------------------------------------------------------------
| DIAGNOSA
|--------------------------------------------------------------------------
*/
Route::get('/diagnosa', function () {

    if (!session()->has('login')) {
        return redirect('/login');
    }

    return view('diagnosa');
});

/*
|--------------------------------------------------------------------------
| PROSES CBR
|--------------------------------------------------------------------------
*/
Route::post('/proses', [CBRController::class, 'proses']);

/*
|--------------------------------------------------------------------------
| RIWAYAT
|--------------------------------------------------------------------------
*/
Route::get('/riwayat', [CBRController::class, 'riwayat']);

/*
|--------------------------------------------------------------------------
| LOGOUT
|--------------------------------------------------------------------------
*/
Route::get('/logout', [CBRController::class, 'logout']);